pub fn reverse_map(n: f64) -> (u16, u16)
{
	let x = (n * ((1 << 16) as f64)).floor() as u16;
	let y = ((n * ((1 << 16) as f64)).fract() * ((1 << 16) as f64)) as u16;
	return (x, y);
}
